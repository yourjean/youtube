package com.study.youtubeteam.emtity;

public class Chat {
	private String chat_id;
	private String chat_img;
	private String chat_area;
	public String getChat_id() {
		return chat_id;
	}
	public void setChat_id(String chat_id) {
		this.chat_id = chat_id;
	}
	public String getChat_img() {
		return chat_img;
	}
	public void setChat_img(String chat_img) {
		this.chat_img = chat_img;
	}
	public String getChat_area() {
		return chat_area;
	}
	public void setChat_area(String chat_area) {
		this.chat_area = chat_area;
	}
	
	
}
